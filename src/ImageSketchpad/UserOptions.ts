/**
 * Image sketchpad user options
 */
export class UserOptions {
  /**
   * Line width
   */
  lineWidth?: number;

  /**
   * Line max width
   */
  lineMaxWidth?: number;

  /**
   * Line color
   */
  lineColor?: string;

  /**
   * Line cap
   */
  lineCap?: CanvasLineCap;

  /**
   * Line join
   */
  lineJoin?: CanvasLineJoin;

  /**
   * Line miter limit
   */
  lineMiterLimit?: number;
}
